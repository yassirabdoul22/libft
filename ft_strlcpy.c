#include "libft.h"
#include <stdio.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	if (size == 0)
	{
		return (ft_strlen(src));
	}
	i = 0;
	if (size)
	{
		while (src[i] && i < size - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	while (src[i])
		i++;
	return (i);
}

int	main(void)
{
	char	*s;

	s = malloc(sizeof(char) * 6);
	ft_strlcpy(s, "helloworld", 6);
	ft_strlcpy(s + 2, s, 6);
	printf("%s", s);
}
